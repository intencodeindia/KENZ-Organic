                Sheep are susceptible to various diseases that can impact their health and productivity. At Kenz, we prioritize strict health monitoring and preventive care to ensure their well-being.
                <strong>Foot Rot</strong> is a bacterial infection that affects the hooves, causing lameness and pain. Regular hoof trimming, dry bedding, and proper hygiene help prevent its occurrence.
                <strong>Blue Tongue</strong> is a viral disease transmitted by insects, leading to fever, swelling, and breathing difficulties. Vaccination and insect control are essential preventive measures.
                <strong>Enterotoxemia (Pulpy Kidney Disease)</strong> is caused by a bacterial toxin, often affecting rapidly growing lambs. Proper vaccination and diet management help in prevention.
                <strong>Parasitic Infections</strong> from worms and external parasites can lead to weight loss, anemia, and reduced wool quality. Regular deworming and pasture rotation help minimize infestations.
                At Kenz, we focus on <strong>preventive healthcare</strong>, vaccinations, and proper flock management to maintain the health and productivity of our sheep.
              </p>
