              <h6 class="livestock-all-headline">Camel Diseases</h6>
              <p class="livestock-all-text">
                Camels, like other livestock, are prone to various diseases that can impact their health and productivity. At Kenz, we implement strict health monitoring and preventive care measures.
                <strong>Trypanosomiasis (Surra)</strong> is a deadly parasitic disease transmitted by flies, causing fever, weight loss, and weakness. Regular veterinary check-ups and insect control are essential.
                <strong>Camelpox</strong> is a viral infection that leads to skin lesions, fever, and respiratory issues. Vaccination and hygiene management help prevent outbreaks.
                <strong>Helminthiasis</strong>, caused by parasitic worms, affects digestion and overall health. Regular deworming and proper grazing management are key preventive measures.
                <strong>Respiratory Infections</strong> can spread due to dust and poor ventilation, leading to coughing and breathing difficulties. Providing clean, well-ventilated shelters reduces the risk.
                <strong>Mastitis</strong> is a bacterial infection affecting lactating camels, leading to swollen udders and reduced milk production. Proper milking hygiene and veterinary care help prevent it.
                At Kenz, we emphasize <strong>preventive healthcare</strong>, regular vaccinations, and biosecurity measures to keep our camels healthy, ensuring their well-being and productivity.
              </p>

