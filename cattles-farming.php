              <h6 class="livestock-all-headline">Cattle Diseases</h6>
              <p class="livestock-all-text">
                Cattle, like other livestock, are susceptible to a variety of diseases that can impact their health and productivity. At Kenz, we prioritize strict health monitoring and preventive care to ensure optimal well-being.
                <strong>Bovine Tuberculosis (TB)</strong> is a contagious bacterial disease affecting the lungs, causing coughing, weight loss, and reduced milk production. Regular testing and biosecurity measures are essential to prevent its spread.
                <strong>Foot and Mouth Disease (FMD)</strong> is a viral infection that causes fever, blisters, and lameness in cattle. Vaccination and strict quarantine protocols help control outbreaks.
                <strong>Brucellosis</strong> is a bacterial infection that affects reproduction, leading to abortion and infertility. Preventive vaccination and strict herd management practices are vital in controlling its spread.
                <strong>Parasitic Infections</strong> caused by worms and external parasites can lead to digestive issues, weight loss, and reduced performance. Regular deworming and proper pasture management are key preventive measures.
                <strong>Mastitis</strong>, a bacterial infection affecting lactating cows, can lead to swollen udders and reduced milk yield. Maintaining proper milking hygiene and veterinary care are essential to prevent this condition.
                At Kenz, we emphasize <strong>preventive healthcare</strong>, regular vaccinations, and biosecurity measures to maintain the health and productivity of our cattle, ensuring their long-term well-being.
              </p>
