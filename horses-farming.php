              <p class="livestock-all-text">
                Horses, like any other animal, are susceptible to a variety of diseases that can affect their health and performance. At Kenz, we take proactive steps to monitor and prevent common diseases.
                <strong>Colic</strong> is a common digestive disorder in horses, causing severe abdominal pain. Regular feeding schedules and access to clean water can help prevent it.
                <strong>Laminitis</strong>, an inflammation of the hooves, can be triggered by poor diet, excessive weight, or other factors. Proper hoof care and a balanced diet are essential for prevention.
                <strong>Equine Influenza</strong> is a contagious respiratory disease that can spread rapidly among horses. Regular vaccination is crucial to protect against this illness.
                <strong>Rabies</strong> is a fatal viral disease that can affect horses through bites from infected animals. Vaccination and regular health checks are important to prevent exposure.
                Another concern is <strong>Strangles</strong>, a highly contagious bacterial infection affecting the lymph nodes. Isolating infected horses and maintaining good hygiene can prevent its spread.
                At Kenz, we prioritize <strong>preventive care</strong> and work closely with veterinarians to ensure our horses remain healthy and disease-free, ensuring optimal performance and well-being.
              </p>
